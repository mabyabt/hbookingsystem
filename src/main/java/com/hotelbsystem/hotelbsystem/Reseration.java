package com.hotelbsystem.hotelbsystem;

public class Reseration {

    String name;
    String CheckIn;
    String CheckOut;
    int numberOfNights;
    int NumberOfPersons;
public Reseration( String name , String CheckIn, String CheckOut, int NumberOfNights, int NumberOfPerson){

    this.name = name;
    this.CheckIn  = CheckIn;
    this.CheckOut = CheckOut;
    this.numberOfNights = NumberOfNights;
    this.NumberOfPersons = NumberOfPerson;
}

}
